/**
 * Get DOO knowledge for a specific topic
 */
export function getDOOKnowledge(topic?: string): string {
  if (!topic) return ALL_DOO_KNOWLEDGE
  
  const topicLower = topic.toLowerCase()
  
  if (topicLower.includes('hr') || topicLower.includes('policy') || topicLower.includes('policies')) {
    return DOO_HR_POLICY
  }
  
  if (topicLower.includes('leave') || topicLower.includes('vacation') || topicLower.includes('holiday') || topicLower.includes('sick') || topicLower.includes('maternity') || topicLower.includes('paternity')) {
    return DOO_LEAVE_QUICK_REFERENCE + '\n\n' + DOO_HR_POLICY
  }
  
  if (topicLower.includes('team') || topicLower.includes('who')) {
    return DOO_TEAM_CONTEXT
  }
  
  if (topicLower.includes('company') || topicLower.includes('about') || topicLower.includes('culture') || topicLower.includes('values')) {
    return DOO_COMPANY_INFO
  }
  
  if (topicLower.includes('conduct') || topicLower.includes('dress') || topicLower.includes('behavior')) {
    return DOO_HR_POLICY
  }
  
  if (topicLower.includes('confidential') || topicLower.includes('data') || topicLower.includes('security')) {
    return DOO_HR_POLICY
  }
  
  return ALL_DOO_KNOWLEDGE
}

